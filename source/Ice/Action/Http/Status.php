namespace Ice\Action;

use Ice\Core\Action;
use Ice\Core\Environment;
use Ice\Helper\Http;

/**
 * Class Http_Status_400
 *
 * Action for page with status 400
 *
 * @see Ice\Core\Action
 * @see Ice\Core\Action_Context
 *
 * @author dp <denis.a.shestakov@gmail.com>
 *
 * @package    Ice
 * @subpackage Action
 *
 * @version 0.0
 * @since   0.0
 */
class Http_Status extends Action
{
    /**
     * Action config
     *
     * example:
     * ```php
     *  $config = [
     *      'actions' => [
     *          ['Ice:Title', ['title' => 'page title'], 'title'],
     *          ['Ice:Another_Action, ['param' => 'value']
     *      ],
     *      'view' => [
     *          'layout' => Emmet::PANEL_BODY,
     *          'template' => _Custom,
     *          'viewRenderClass' => Ice:Twig,
     *      ],
     *      'input' => [
     *          Request::DEFAULT_DATA_PROVIDER_KEY => [
     *              'paramFromGETorPOST => [
     *                  'default' => 'defaultValue',
     *                  'validators' => ['Ice:PATTERN => PATTERN::LETTERS_ONLY]
     *                  'type' => 'string'
     *              ]
     *          ]
     *      ],
     *      'output' => ['Ice:Resource/Ice\Action\Index'],
     *      'ttl' => 3600,
     *      'roles' => []
     *  ];
     * ```
     *
     * @return array
     *
     * @author anonymous <email>
     *
     * @version 0
     * @since   0
     */
    protected static function config()
    {
        return [
            'view' => ['viewRenderClass' => 'Ice:Php'],
            'input' => [
                'code' => ['default' => 500],
                'exception'
            ]
        ];
    }

    /**
     * Run action
     *
     * @param  array $input
     * @return array
     *
     * @author dp <denis.a.shestakov@gmail.com>
     *
     * @version 0.0
     * @since   0.0
     */
    public function run(array $input)
    {
        Http::setHeader(Http::getStatusCodeHeader($input['code']), true, $input['code']);

        $this->getView()->setTemplate('_' . $input['code']);

        return isset($input['exception']) && !Environment::getInstance()->isProduction()
            ? [
                'message' => $input['exception']->getMessage(),
                'stackTrace' => nl2br(str_replace(dirname(MODULE_DIR), '', $input['exception']->getTraceAsString()))
            ]
            : ['message' => '', 'stackTrace' => ''];
    }
}
